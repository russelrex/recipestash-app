const ALL_EMOJIS = Object.values(EMOJI_CATEGORIES).flat();

export default function EmojiPicker({ visible, onClose, onEmojiSelect }: EmojiPickerProps) {
  const [searchQuery, setSearchQuery] = useState('');
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof EMOJI_CATEGORIES>('frequent');

  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const getFilteredEmojis = () => {
    if (searchQuery.trim()) {
      // Simple search - filter emojis that might match (this is basic, could be improved)
      return ALL_EMOJIS.filter(emoji => 
        emoji.includes(searchQuery) || 
        searchQuery.toLowerCase().includes(emoji)
      );
    }
    return EMOJI_CATEGORIES[selectedCategory] || [];
  };

  const filteredEmojis = getFilteredEmojis();

  return (
    <Modal
      visible={visible}
      transparent
      animationType="slide"
      onRequestClose={onClose}
    >
      <View style={styles.modalOverlay}>
        {/* Backdrop - tap to close */}
        <TouchableOpacity 
          style={styles.backdrop} 
          activeOpacity={1} 
          onPress={onClose}
        />
        
        {/* Emoji Picker Container */}
        <View style={styles.pickerContainer}>
          {/* Header */}
          <View style={styles.header}>
            <Text variant="titleMedium" style={styles.headerTitle}>
              Choose Emoji
            </Text>
            <IconButton
              icon="close"
              size={24}
              onPress={onClose}
              iconColor={Colors.text.primary}
            />
          </View>

          {/* Search Bar */}
          <View style={styles.searchContainer}>
            <Searchbar
              placeholder="Search emojis..."
              onChangeText={setSearchQuery}
              value={searchQuery}
              style={styles.searchBar}
              iconColor={Colors.primary.main}
            />
          </View>

          {/* Category Tabs */}
          {!searchQuery && (
            <ScrollView 
              horizontal 
              showsHorizontalScrollIndicator={false}
              style={styles.categoryTabs}
              contentContainerStyle={styles.categoryTabsContent}
            >
              {Object.keys(EMOJI_CATEGORIES).map((category) => (
                <TouchableOpacity
                  key={category}
                  style={[
                    styles.categoryTab,
                    selectedCategory === category && styles.categoryTabActive
                  ]}
                  onPress={() => setSelectedCategory(category as keyof typeof EMOJI_CATEGORIES)}
                >
                  <Text style={[
                    styles.categoryTabText,
                    selectedCategory === category && styles.categoryTabTextActive
                  ]}>
                    {category === 'frequent' ? 'â­' : category === 'smileys' ? 'ğŸ˜Š' : category === 'food' ? 'ğŸ•' : category === 'activities' ? 'âš½' : category === 'nature' ? 'ğŸŒ²' : category === 'objects' ? 'ğŸ“±' : 'â¤ï¸'}
                  </Text>
                </TouchableOpacity>
              ))}
            </ScrollView>
          )}

          {/* Emoji Grid */}
          <ScrollView style={styles.emojiGrid} contentContainerStyle={styles.emojiGridContent}>
            {filteredEmojis.length === 0 ? (
              <View style={styles.emptyContainer}>
                <Icon name="emoticon-sad-outline" size={48} color={Colors.text.disabled} />
                <Text variant="bodyMedium" style={styles.emptyText}>
                  No emojis found
                </Text>
              </View>
            ) : (
              <View style={styles.emojiRow}>
                {filteredEmojis.map((emoji, index) => (
                  <TouchableOpacity
                    key={`${emoji}-${index}`}
                    style={styles.emojiButton}
                    onPress={() => handleEmojiSelect(emoji)}
                    activeOpacity={0.7}
                  >
                    <Text style={styles.emoji}>{emoji}</Text>
                  </TouchableOpacity>
                ))}
              </View>
            )}
          </ScrollView>
        </View>
      </View>
    </Modal>
  );
}

